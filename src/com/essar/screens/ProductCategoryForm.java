    @Override
    public void actionPerformed(ActionEvent event) {
        try{
            JMenuItem menu = (JMenuItem) event.getSource();
            if (menu == mitDelete) {
                //int rowIndex = tblCategory.getSelectedRow();
                //System.out.println("--------INSIDE DELETE-"+tblCategory.getModel().getValueAt(tblCategory.getSelectedRow(),0));
                String productCategoryCode = tblCategory.getModel().getValueAt(tblCategory.getSelectedRow(),0).toString();
                ProductCategoryDAO productCategoryDAO = new ProductCategoryDAO();
                productCategoryDAO.deleteRecordByCode(productCategoryCode);
                //initializeForm();
                GenericUtils.removeRowFromJTable(tblCategory, productCategoryModel);
            } else if (menu == mitEdit) {
                syncDataFromViewToTable();
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
    }
