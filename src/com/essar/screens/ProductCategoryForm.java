    public void enableFormElements(boolean flag){
        txtProductCategoryCode.setEnabled(flag);
        txtProductCategoryName.setEnabled(flag);
    }
    public boolean validateForm(){
        //btnSave.setText("Add");
        //btnSave.requestFocus();
        if(null == txtProductCategoryCode.getText() || !(txtProductCategoryCode.getText().matches("\\w+"))){
            JOptionPane.showMessageDialog(null, "The product category code "+txtProductCategoryCode.getText() +" is not valid.");
            return false;
        }
        if(null == txtProductCategoryName.getText() || !(txtProductCategoryName.getText().matches("\\w+"))){
            JOptionPane.showMessageDialog(null, "The product category name "+txtProductCategoryName.getText() +" is not valid.");
            return false;
        }
        //txtProductCategoryName.setText("");
        return true;
    }
    
    public void saveProductCategoryForm(){ 
        ProductCategory productCategory = new ProductCategory();
        productCategory.setProductCategoryCode(txtProductCategoryCode.getText());
        productCategory.setProductCategoryName(txtProductCategoryName.getText());        
        ProductCategoryDAO productCategoryDAO = new ProductCategoryDAO();
        productCategoryDAO.insertIntoDB(productCategory);        
    }
    
    public void updateProductCategoryForm(){ 
        ProductCategory productCategory = new ProductCategory();
        productCategory.setProductCategoryId(productCategoryId);
        productCategory.setProductCategoryCode(txtProductCategoryCode.getText());
        productCategory.setProductCategoryName(txtProductCategoryName.getText());        
        ProductCategoryDAO productCategoryDAO = new ProductCategoryDAO();
        productCategoryDAO.updateProductCategory(productCategory);        
    }
    
    public void renderDataIntoTable(List<ProductCategory> productCategoryList){
        //DefaultTableModel productCategoryModel = null;
        productCategoryModel = (DefaultTableModel) tblCategory.getModel();
        //System.out.println("-----"+productCategoryList.size());
        for(int i=0;i<productCategoryList.size();i++){
            productCategoryModel.addRow(new Object[]{productCategoryList.get(i).getProductCategoryCode()
                    ,productCategoryList.get(i).getProductCategoryName()
                    ,productCategoryList.get(i).getProductCategoryId()});
        //model.
        }
        
        tblCategory.addMouseListener(new MouseAdapter() {
            public void mouseClicked(MouseEvent me) {
                if (me.getClickCount() == 2) {     // to detect doble click events
                    syncDataFromViewToTable();
                }
            }
        });
        
    }
    
    public void syncDataFromViewToTable(){
        ProductCategory productCategory= null;
        System.out.println("--------INSIDE Edit-"+tblCategory.getModel().getValueAt(tblCategory.getSelectedRow(),0));
        productCategoryId = Integer.parseInt(tblCategory.getModel().getValueAt(tblCategory.getSelectedRow(),tblCategory.getColumnCount()-1).toString());
        ProductCategoryDAO productCategoryDAO = new ProductCategoryDAO();
        productCategory = productCategoryDAO.retrieveById(productCategoryId);

        txtProductCategoryName.setText(productCategory.getProductCategoryName());
        txtProductCategoryCode.setText(productCategory.getProductCategoryCode());
        enableFormElements(true);
        btnSave.setText("Update");
        btnSave.setIcon(new javax.swing.ImageIcon(getClass().getResource("/images/update.png")));
    }

